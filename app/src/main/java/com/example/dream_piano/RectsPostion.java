package com.example.dream_piano;

public class RectsPostion {
    private int mLeftPos;
    private int mRightPos;
    private int mTopPos;
    private int mBottomPos;

    public void setmBottomPos(int mBottomPos) {
        this.mBottomPos = mBottomPos;
    }
    public void setmLeftPos(int mLeftPos) {
        this.mLeftPos = mLeftPos;
    }
    public void setmRightPos(int mRightPos) {
        this.mRightPos = mRightPos;
    }
    public void setmTopPos(int mTopPos) {
        this.mTopPos = mTopPos;
    }


    public int getmBottomPos() {
        return mBottomPos;
    }
    public int getmLeftPos() {
        return mLeftPos;
    }
    public int getmRightPos() {
        return mRightPos;
    }
    public int getmTopPos() {
        return mTopPos;
    }
}
